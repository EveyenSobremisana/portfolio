          <p class="project_process_title">Final Result </p>

          <p class="projectBody">Although it does not completely resemble the original picture, I feel really proud of this illustration because it challenged my creative judgment. It made me think about how to execute some parts of the console to make it look like the original picture. Sometimes, I make a lot of variation and ask an opinion about which one should I use. The hardest part of this process was the gradient and choosing the right colors. To make it look similar, I have to choose the right colors, make it into a global color and adjust the tint to make it brighter or lighter. There is one detail that I was not able to do in this project. If you look closely to the original picture, there is a grainy texture on the surface of the console. I was not able to do it due to lack of time. However, I was able to add texture on the joystick of the console using gradient mesh and some effects available on Illustrator.
          </p>

