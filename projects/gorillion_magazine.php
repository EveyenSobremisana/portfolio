          <p class="project_process_title">Development </p>



          <p class="projectBody">To be able to isolate the animals from the background, I converted both images into a <em>smart layer </em> so when I mask the pictures, it won't be destructive. I used the refine edge tool brush to select the unwanted grass and other backgrounds that I don't want. Then, I decided if I would go with the color of the Gorilla (ash) or go with the color of the Lion (Dark to Light Tawny brown). I decided to go with Lion's fur color and <em>color filled</em> the gorilla's body with the color that I picked from the Lion. Then I replaced the lions head and tail into the gorilla's body so I can start cloning some of the hair from the Lion to the body of the Gorilla for a natural feel. I did some adjustments on the lights and darks for my hybrid animal to even out the colors and the clones. After I finished the hybrid animal, I placed the background that I selected and started putting text and converting them as an outlined text.
          </p>


